import { readFileSync, writeFileSync, existsSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const envPath = join(root, ".env");

function parseEnv(text) {
  var out = {};
  text.split(/\r?\n/).forEach(function (line) {
    var trimmed = line.trim();
    if (!trimmed || trimmed[0] === "#") return;
    var eq = trimmed.indexOf("=");
    if (eq === -1) return;
    var key = trimmed.slice(0, eq).trim();
    var val = trimmed.slice(eq + 1).trim();
    if (
      (val[0] === '"' && val[val.length - 1] === '"') ||
      (val[0] === "'" && val[val.length - 1] === "'")
    ) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  });
  return out;
}

var fileEnv = {};
if (existsSync(envPath)) {
  fileEnv = parseEnv(readFileSync(envPath, "utf8"));
}

var url = process.env.SUPABASE_URL || fileEnv.SUPABASE_URL || "";
var anonKey = process.env.SUPABASE_ANON_KEY || fileEnv.SUPABASE_ANON_KEY || "";

var payload = { url: url, anonKey: anonKey };
var out =
  "/* Generated by npm run env:gen — do not edit by hand */\n" +
  "window.__BUILDBET_SUPABASE = " +
  JSON.stringify(payload, null, 0) +
  ";\n";

writeFileSync(join(root, "supabase-env.js"), out, "utf8");

if (!url || !anonKey) {
  console.warn(
    "env:gen: SUPABASE_URL or SUPABASE_ANON_KEY is empty. Set them in .env or the environment, then run again."
  );
} else {
  console.log("Wrote supabase-env.js");
}
